var abrirVamp = document.getElementById('abrir-vamp'),
    overlayVamp = document.getElementById('overlay-vamp'),
    cerrarVamp = document.getElementById('cerrar-vamp');

abrirVamp.addEventListener('click', function()
{
    overlayVamp.classList.add('active')
})

cerrarVamp.addEventListener('click', function()
{
    overlayVamp.classList.remove('active')
})

